function buildShapeDataUri(shapeName: string): string {
  const shape = shapePaths[shapeName];
  if (!shape) return buildShapeDataUri("blob");
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${shape.viewBox}"><path fill="black" d="${shape.d}"/></svg>`;
  return `data:image/svg+xml,${encodeURIComponent(svg)}`;
}

export function getToolIcon(iconName?: string): LucideIcon {
  if (!iconName) return Layers;
  return iconMap[iconName] ?? Layers;
}

export function getToolShape(iconName?: string): string {
  const shapeName = iconName ? (shapeMap[iconName] ?? "blob") : "blob";
  return buildShapeDataUri(shapeName);
}
